package com.articoding.model.articodingLevel.initialState;

public class ACBlocklyVariable {
    private String type;
    private String id;
    private String text;

    public ACBlocklyVariable() {
    }

    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getText() {
        return text;
    }

    public void setText(String text) {
        this.text = text;
    }
}
